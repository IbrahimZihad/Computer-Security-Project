# --- Functions for AES Decryption ---
def print_state(state, label):
    print(f"\n{label}:")
    for row in range(4):
        print("  ", end="")
        for col in range(4):
            print(f"{state[row][col]:02x} ", end="")
        print()

def bytes_to_state(data):
    state = [[0]*4 for _ in range(4)]
    for i in range(16):
        state[i%4][i//4] = data[i]
    return state

def state_to_bytes(state):
    data = []
    for col in range(4):
        for row in range(4):
            data.append(state[row][col])
    return bytes(data)

def add_round_key(state, round_key):
    for row in range(4):
        for col in range(4):
            state[row][col] ^= round_key[row][col]
    return state

def key_expansion(key):
    key_schedule = [key[i:i+4] for i in range(0,16,4)]
    for round_num in range(1,11):
        temp = list(key_schedule[-1])
        temp = temp[1:] + temp[:1]
        temp = [S_BOX[b] for b in temp]
        temp[0] ^= RCON[round_num]
        new_word = bytes([temp[i]^key_schedule[-4][i] for i in range(4)])
        key_schedule.append(new_word)
        for _ in range(3):
            new_word = bytes([key_schedule[-1][i]^key_schedule[-4][i] for i in range(4)])
            key_schedule.append(new_word)
    round_keys = []
    for i in range(0,44,4):
        round_keys.append(bytes_to_state(b''.join(key_schedule[i:i+4])))
    return round_keys

def inv_sub_bytes(state):
    for row in range(4):
        for col in range(4):
            state[row][col] = INV_S_BOX[state[row][col]]
    return state

def inv_shift_rows(state):
    state[1] = state[1][-1:] + state[1][:-1]
    state[2] = state[2][-2:] + state[2][:-2]
    state[3] = state[3][-3:] + state[3][:-3]
    return state

def galois_mult(a,b):
    p=0
    for _ in range(8):
        if b & 1: p ^= a
        hi_bit_set = a & 0x80
        a = (a<<1) & 0xFF
        if hi_bit_set: a ^= 0x1b
        b >>=1
    return p

def inv_mix_columns(state):
    for col in range(4):
        s0,s1,s2,s3=state[0][col],state[1][col],state[2][col],state[3][col]
        state[0][col] = galois_mult(0x0e,s0)^galois_mult(0x0b,s1)^galois_mult(0x0d,s2)^galois_mult(0x09,s3)
        state[1][col] = galois_mult(0x09,s0)^galois_mult(0x0e,s1)^galois_mult(0x0b,s2)^galois_mult(0x0d,s3)
        state[2][col] = galois_mult(0x0d,s0)^galois_mult(0x09,s1)^galois_mult(0x0e,s2)^galois_mult(0x0b,s3)
        state[3][col] = galois_mult(0x0b,s0)^galois_mult(0x0d,s1)^galois_mult(0x09,s2)^galois_mult(0x0e,s3)
    return state

def pkcs7_unpad(data):
    pad_len = data[-1]
    if pad_len <1 or pad_len>16: return data
    return data[:-pad_len]

def aes_decrypt_block(block, round_keys):
    state = bytes_to_state(block)
    state = add_round_key(state, round_keys[10])
    for round_num in range(9,0,-1):
        state = inv_shift_rows(state)
        state = inv_sub_bytes(state)
        state = add_round_key(state, round_keys[round_num])
        state = inv_mix_columns(state)
    state = inv_shift_rows(state)
    state = inv_sub_bytes(state)
    state = add_round_key(state, round_keys[0])
    return state_to_bytes(state)

def aes_decrypt(ciphertext, key):
    if isinstance(ciphertext,str):
        ciphertext = bytes.fromhex(ciphertext)
    if len(key)!=16: raise ValueError("Key must be 16 bytes")
    round_keys = key_expansion(key)
    plaintext_padded = b""
    for i in range(0,len(ciphertext),16):
        block = ciphertext[i:i+16]
        plaintext_padded += aes_decrypt_block(block, round_keys)
    return pkcs7_unpad(plaintext_padded)

